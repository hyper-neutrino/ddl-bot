/** Style (color, or link) for a button. */
export enum ButtonStyle {
    /** Primary (Call-To-Action); blue. */
    PRIMARY = 1,
    /** Secondary; grey */
    SECONDARY,
    /** Success (Affirmative Action); green */
    SUCCESS,
    /** Danger (Destructive Action); red */
    DANGER,
    /** Link (navigates to a URL instead of creating an interaction); grey */
    LINK,
}
